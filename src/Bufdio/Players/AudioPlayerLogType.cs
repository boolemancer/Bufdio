namespace Bufdio.Players
{
    /// <summary>
    /// Enumeration represents audio player log type.
    /// </summary>
    public enum AudioPlayerLogType
    {
        /// <summary>
        /// Indicates that the log is an info log.
        /// </summary>
        Info,
        
        /// <summary>
        /// Indicates that the log is a warning log.
        /// </summary>
        Warning,
        
        /// <summary>
        /// Indicates that the log is an error log.
        /// </summary>
        Error
    }
}
